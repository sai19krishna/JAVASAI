
public class Alphabets {
	      public static void main(String args[]){
	            char ch;
	            System.out.println("Small Alphabets: ");
	            for( ch = 'a' ; ch <= 'y' ; ch++ ){
	               System.out.println(ch);
	            }           
	 
	            System.out.println("Capital Alphabets: ");
	            for( ch = 'A' ; ch <= 'Y' ; ch++ ){
	               System.out.println(ch);
	            }
	         }
	}
